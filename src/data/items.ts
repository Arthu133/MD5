export const items: Item[] = blueprints.flatMap((blueprint) =>
  blueprint.names.map((name, index) => ({
    id: slugify(name),
    name,
    icon: `/assets/items/${slugify(name)}.svg`,
    category: blueprint.category,
    description: blueprint.description,
    displayTags: blueprint.displayTags ?? categoryDisplayTags[blueprint.category],
    stats: {
      ...blueprint.stats,
      ...(index === 1 ? { utility: (blueprint.stats.utility ?? 0) + 2 } : {}),
      ...(index === 2 ? { scaling: (blueprint.stats.scaling ?? 0) + 2 } : {}),
    },
    tags: blueprint.tags,
    bestFor: {
      roles: blueprint.roles,
      classes: blueprint.classes,
      damageProfiles: blueprint.damageProfiles,
    },
    badFor: {
      classes: blueprint.badClasses,
      damageProfiles: blueprint.badDamageProfiles,
    },
    uniqueEffect: {
      name: index === 0 ? "Ímpeto" : index === 1 ? "Ressonância" : "Convergência",
      description:
        index === 0
          ? "Reforça o primeiro ponto forte da identidade do campeão."
          : index === 1
            ? "Converte parte do poder em utilidade coletiva."
            : "Ganha valor adicional conforme a partida avança.",
      effects:
        index === 0
          ? { earlyPressure: 2 }
          : index === 1
            ? { utility: 2 }
            : { scaling: 2 },
    },
  })),
);

